// Starts an HTTP server with WebSocket upgrade support.
// Binds to 127.0.0.1 only — never exposed to the network unless explicitly configured.
//
// WebSocket:
//   - Auth via session token as query param: ws://localhost:7474/ws?token=<token>
//   - On connect: immediately sends a full system.status snapshot as the first message.
//   - Subscribes to EventBus and fans out all events to connected clients.
//
// REST:
//   GET  /status                          — current environment snapshot
//   GET  /tasks                           — recent task history (up to 20)
//   GET  /memory/episodic                 — recent episodic entries
//   GET  /memory/durable                  — durable context
//   GET  /policy/allowlist                — package allowlist
//   POST /approvals/:id/approve           — approve a pending Tier 2 token (auth required)
//   POST /approvals/:id/deny              — deny a pending Tier 2 token (auth required)
//
// SAFETY GATE: approval endpoints require Authorization: Bearer <sessionToken>.
// SAFETY GATE: server binds to 127.0.0.1 only (host override must be explicit).

